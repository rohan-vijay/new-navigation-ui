function flat(files, p = '') {
  const o = {}
  for (const f of files) {
    if (f.type === 'folder') Object.assign(o, flat(f.children, p + f.name + '/'))
    else o[p + f.name] = f.content
  }
  return o
}
function applyEdits(map, edits) {
  const out = { ...map }
  for (const [path, fn] of edits) if (out[path] != null) out[path] = fn(out[path])
  return out
}
const dropLine = (sub) => (txt) => txt.split('\n').filter(l => !l.includes(sub)).join('\n')
const replace = (a, b) => (txt) => txt.split(a).join(b)
const cutSection = (heading) => (txt) => {
  const i = txt.indexOf('\n## ' + heading)
  return i === -1 ? txt : txt.slice(0, i) + '\n'
}

