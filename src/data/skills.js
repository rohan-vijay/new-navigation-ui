const runPy = (s) => `"""${s.name} — runtime entrypoint."""
from unify.sdk import skill, tool
from .utils import load_context, normalize


@skill("${s.id}")
def run(payload: dict) -> dict:
    """Execute the ${s.name} skill against an inbound payload."""
    ctx = load_context(payload)
    signals = normalize(ctx["signals"])

    score = 0.0
    for signal, weight in WEIGHTS.items():
        score += signals.get(signal, 0) * weight

    return {
        "status": "ok",
        "account": ctx["account"],
        "score": round(score * 100),
        "tier": tier_for(score),
    }


WEIGHTS = {
    "icp_fit": 0.35,
    "intent": 0.30,
    "engagement": 0.20,
    "timing": 0.15,
}


def tier_for(score: float) -> str:
    if score >= 0.75:
        return "A"
    if score >= 0.5:
        return "B"
    return "C"
`

const utilsPy = () => `"""Shared helpers for GTM skills."""


def load_context(payload: dict) -> dict:
    return {
        "account": payload.get("account"),
        "signals": payload.get("signals", []),
        "stage": payload.get("stage", "discovery"),
    }


def normalize(signals: list[str]) -> dict[str, float]:
    """Map raw signal strings to a 0..1 strength per dimension."""
    buckets = {"icp_fit": 0.0, "intent": 0.0, "engagement": 0.0, "timing": 0.0}
    for s in signals:
        if "pricing" in s or "demo" in s:
            buckets["intent"] = min(1.0, buckets["intent"] + 0.5)
        if "opened" in s or "reply" in s:
            buckets["engagement"] = min(1.0, buckets["engagement"] + 0.3)
        if "renewal" in s:
            buckets["timing"] = 0.8
    buckets["icp_fit"] = 0.7
    return buckets
`

