function olderChain(v, n) {
  let [maj, min] = v.replace(/^v/, '').split('.').map(Number)
  const out = []
  for (let i = 0; i < n; i++) {
    if (min > 0) min--
    else if (maj > 0) { maj--; min = 9 }
    out.push(`v${maj}.${min}.0`)
  }
  return out
}

function buildVersions(s) {
  const maps = [flat(buildFiles(s))]
  for (const step of EDIT_STEPS) maps.push(applyEdits(maps[maps.length - 1], step))
  const labels = [s.version, ...olderChain(s.version, EDIT_STEPS.length)]
  return maps.map((files, i) => {
    const m = VMETA[i] || VMETA[VMETA.length - 1]
    return {
      id: `v-${i}`,
      version: labels[i],
      when: i === 0 ? s.updated : m.when,
      author: i === 0 ? s.owner : m.author,
      init: i === 0 ? s.ownerInit : m.init,
      files,
    }
  })
}

